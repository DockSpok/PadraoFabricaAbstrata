package entidade;

public class Moto {
	protected String nome;
	protected String cor;
	protected String cilindrada;
	protected String valor;
	
	public String getNome() {
		return nome;
	}
	public String getCor() {
		return cor;
	}
	public String getCilindrada() {
		return cilindrada;
	}
	public String getValor() {
		return valor;
	}
}
